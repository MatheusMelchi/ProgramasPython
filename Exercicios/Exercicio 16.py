if int(nm) == 1:
    print("Janeiro")
elif int(nm) == 2:
    print("Feveiro")
elif int(nm) == 3:
    print("Março")
elif int(nm) == 4:
    print("Abril")
elif int(nm) == 5:
    print("Maio")
elif int(nm) == 6:
    print("Junho")
elif int(nm) == 7:
    print("Julho")
elif int(nm) == 8:
    print("Agosto")
elif int(nm) == 9:
    print("Setembro")
elif int(nm) == 10:
    print("Outubro")
elif int(nm) == 11:
    print("Novembro")
elif int(nm) == 12:
    print("Dezembro")
else:
    print(f"{int(nm)} Mês? Bom, não existe")